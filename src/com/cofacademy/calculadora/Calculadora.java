package com.cofacademy.calculadora;

public class Calculadora {

    public float operacionSuma (float num1,float num2){
        return num1 + num2;

    }
    public float operacioResta (float num1,float num2){
        return num1 - num2;
    }
    public float operacionMultiplicacion (float num1,float num2){
        return num1 * num2;
    }
    public float operacioDivision (float num1,float num2){
        return num1 / num2;
    }



}
